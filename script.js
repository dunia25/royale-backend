document.addEventListener("DOMContentLoaded", function () {
    document.querySelectorAll(".product-card").forEach(card => {
        let addToCartButton = card.querySelector(".add-to-cart-btn");
        
        if (addToCartButton) {
            addToCartButton.addEventListener("click", function () {
                let productName = card.querySelector("h3").textContent;
                let productPrice = parseFloat(card.querySelector(".product-price").textContent.replace("$", ""));
                let productImage = card.querySelector("img").src;

                let cartItems = JSON.parse(localStorage.getItem("cart")) || [];

                let existingItem = cartItems.find(item => item.name === productName);
                if (existingItem) {
                    existingItem.quantity += 1;
                } else {
                    cartItems.push({ name: productName, price: productPrice, quantity: 1, image: productImage });
                }

                localStorage.setItem("cart", JSON.stringify(cartItems));

                // تحديث العرض دون الحاجة لإعادة تحميل الصفحة
                updateCartUI(cartItems);

                alert(`✅ تمت إضافة ${productName} إلى السلة!`);
            });
        }
    });

    function updateCartUI(cartItems) {
        let cartContainer = document.getElementById("cart-items");
        if (!cartContainer) return;

        cartContainer.innerHTML = "";
        cartItems.forEach((item, index) => {
            cartContainer.innerHTML += `
                <div class="cart-item">
                    <img src="${item.image}" alt="${item.name}">
                    <p>${item.name} - ${item.price} $ × ${item.quantity}</p>
                    <button onclick="removeItem(${index})">🗑️ حذف</button>
                </div>
            `;
        });
    }
});


document.addEventListener("DOMContentLoaded", function () {
    let loyaltyPoints = JSON.parse(localStorage.getItem("loyaltyPoints")) || 0;
    let pointsDisplay = document.getElementById("loyalty-points");

    if (pointsDisplay) {
        pointsDisplay.innerText = loyaltyPoints;
    }

    function addPoints(amount) {
        loyaltyPoints += amount;
        localStorage.setItem("loyaltyPoints", JSON.stringify(loyaltyPoints));
        if (pointsDisplay) {
            pointsDisplay.innerText = loyaltyPoints;
        }
        alert(`🎉 تم إضافة ${amount} نقطة إلى حسابك!`);
    }

    function redeemPoints() {
        if (loyaltyPoints >= 100) {
            alert("🎁 تهانينا! يمكنك استبدال نقاطك بخصم أو هدية.");
            loyaltyPoints -= 100;
            localStorage.setItem("loyaltyPoints", JSON.stringify(loyaltyPoints));
            if (pointsDisplay) {
                pointsDisplay.innerText = loyaltyPoints;
            }
        } else {
            alert("❌ تحتاج إلى 100 نقطة على الأقل لاستبدالها.");
        }
    }

    window.addPoints = addPoints;
    window.redeemPoints = redeemPoints;
});


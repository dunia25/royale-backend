function getBeautyRecommendation() {
    let skinType = document.getElementById("skin-type").value;
    let darkCircles = document.getElementById("dark-circles").value;
    let skinElasticity = document.getElementById("skin-elasticity").value;
    let acne = document.getElementById("acne").value;
    let morningSkin = document.getElementById("morning-skin").value;

    let recommendations = [];

    if (skinType === "جافة" && morningSkin === "متعبة وباهتة") {
        recommendations.push("كريم الترطيب المكثف وسيروم التفتيح لإعادة الحيوية للبشرة.");
    }
    if (darkCircles === "نعم، بشكل واضح") {
        recommendations.push("كريم تحت العين بمستخلصات طبيعية لتخفيف الهالات السوداء.");
    }
    if (skinElasticity === "نعم، أحتاج منتجات شد البشرة") {
        recommendations.push("سيروم شد البشرة وكريم يحتوي على الكولاجين للحفاظ على المرونة.");
    }
    if (acne === "نعم، بشكل مستمر") {
        recommendations.push("غسول طبي ومقشر خفيف لتنظيم إفرازات البشرة وتقليل حب الشباب.");
    }

    let resultText = recommendations.length > 0
        ? "🚀 بناءً على إجاباتكِ، نوصي بـ: " + recommendations.join(" ")
        : "منتجات متنوعة تناسب احتياجات بشرتكِ المختلفة!";

    document.getElementById("beauty-results").innerHTML = `<p>${resultText}</p>`;
}


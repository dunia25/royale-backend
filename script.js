document.addEventListener("DOMContentLoaded", function () {
    let siteReviews = JSON.parse(localStorage.getItem("siteReviews")) || [];
    let reviewsContainer = document.getElementById("site-reviews-container");

    function displaySiteReviews() {
        if (!reviewsContainer) return;
        reviewsContainer.innerHTML = siteReviews.length ? siteReviews.map(review => `<p>⭐ ${review.rating} - ${review.text}</p>`).join("") : "<p>لا توجد مراجعات بعد.</p>";
    }

    function submitSiteReview() {
        let reviewText = document.getElementById("site-review").value.trim();
        let rating = localStorage.getItem("siteRating") || 0;

        if (!reviewText) {
            alert("❌ يرجى كتابة تعليق قبل الإرسال.");
            return;
        }

        let review = { rating, text: reviewText };
        siteReviews.push(review);
        localStorage.setItem("siteReviews", JSON.stringify(siteReviews));

        displaySiteReviews();
        alert("✅ تم إرسال التقييم بنجاح!");
    }

    displaySiteReviews();
    window.submitSiteReview = submitSiteReview;
});

